$(document).ready(function() {
  $("input[name='cemail']").keyup(function() {
      var cemail = $("input[name='cemail']").val();
      $.post("contactform-validation.php", {cemail: cemail}, function(data, status) {
        $("#contactcemail").html(data);
      });
    });

    $("textarea[name='cmessage']").keyup(function() {
      var cmessage = $("textarea[name='cmessage']").val();
      $.post("contactform-validation.php", {cmessage: cmessage}, function (data, status) {
        $("#messagecemail").html(data);
      });
    });

    $("button[name='submit']").click(function() {
      var cemail = $("input[name='cemail']").val();
      var cmessage = $("textarea[name='cmessage']").val();
      var submit1 = true;

      var cform = new FormData();
      cform.append('cemail', cemail);
      cform.append('cmessage', cmessage);
      cform.append('submit', submit1);

      $.ajax({
        url: 'contactform-validation.php',
        dataType: 'text',
        cache: false,
        contentType: false,
        processData: false,
        data: cform,
        type: 'post',
        success: function(response) {
          console.log(response);
          var errorarr = response.split('|.|');
          if(errorarr[1] == 'error') {
            $("#outcomeofcform").html("Could not submit the contact form due to errors");
            $("#outcomeofcform").removeClass('green');
            $("#outcomeofcform").addClass('highlight');
            $("input[name='cemail']").keyup();
            $("textarea[name='cmessage']").keyup();
          }
          if(errorarr[1] == 'success') {
            $("#outcomeofcform").html("The contact form has been submitted");
            $("#outcomeofcform").removeClass('highlight');
            $("#outcomeofcform").addClass('green');
            $("input[name='cemail']").val("");
            $("textarea[name='cmessage']").val("");
          }
        }
      });

    });
});


